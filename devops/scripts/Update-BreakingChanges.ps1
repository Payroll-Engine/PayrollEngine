# -- Helpers -------------------------------------------------------------------

$script:report = [System.Collections.Generic.List[string]]::new()
$script:breakingCount = 0
$script:warningCount = 0
$script:currentSectionKey = ""
$script:sectionBreaks = @{}

# RELEASE_NOTES.md section mapping
$script:releaseNotesPath = "$ReposRoot\PayrollEngine\RELEASE_NOTES.md"
$script:releaseNotesMap = @{
    RestApi        = @{ Header = "Backend";          Url = "https://github.com/Payroll-Engine/PayrollEngine.Backend"; IsLibrary = $false }
    BackendApi     = @{ Header = "Backend";          Url = "https://github.com/Payroll-Engine/PayrollEngine.Backend"; IsLibrary = $false }
    ScriptingApi   = @{ Header = "Client Scripting"; Url = "https://github.com/Payroll-Engine/PayrollEngine.Client.Scripting"; IsLibrary = $true }
    ClientServices = @{ Header = "Client Services"; Url = "https://github.com/Payroll-Engine/PayrollEngine.Client.Services"; IsLibrary = $true }
}

function Write-Section([string]$title) {
    $line = "-" * 70
    Write-Host ("`n" + $line) -ForegroundColor DarkGray
    Write-Host ("  " + $title) -ForegroundColor Cyan
    Write-Host $line -ForegroundColor DarkGray
    $script:report.Add("`n## " + $title + "`n")
}

function Write-Breaking([string]$message) {
    Write-Host "  BREAKING: $message" -ForegroundColor Red
    $script:report.Add("- **BREAKING:** $message")
    $script:breakingCount++

    if ($script:currentSectionKey) {
        if (-not $script:sectionBreaks.ContainsKey($script:currentSectionKey)) {
            $script:sectionBreaks[$script:currentSectionKey] = [System.Collections.Generic.List[string]]::new()
        }
        $script:sectionBreaks[$script:currentSectionKey].Add($message)
    }
}

function Write-Warn([string]$message) {
    Write-Host "  WARNING:  $message" -ForegroundColor Yellow
    $script:report.Add("- WARNING: $message")
    $script:warningCount++
}

function Write-Ok([string]$message) {
    Write-Host "  OK:      $message" -ForegroundColor Green
}

function Get-SafeProperty($obj, [string]$name, $default = $null) {
    if ($null -eq $obj) { return $default }
    $prop = $obj.PSObject.Properties[$name]
    if ($null -eq $prop) { return $default }
    return $prop.Value
}

function Invoke-Git {
    param([string]$WorkDir, [string[]]$Arguments)
    $pinfo = New-Object System.Diagnostics.ProcessStartInfo
    $pinfo.FileName = "git"
    $pinfo.Arguments = $Arguments -join " "
    $pinfo.WorkingDirectory = $WorkDir
    $pinfo.RedirectStandardOutput = $true
    $pinfo.RedirectStandardError = $true
    $pinfo.UseShellExecute = $false
    $pinfo.CreateNoWindow = $true
    $proc = [System.Diagnostics.Process]::Start($pinfo)
    $stdout = $proc.StandardOutput.ReadToEnd()
    $null = $proc.StandardError.ReadToEnd()
    $proc.WaitForExit()
    return @{ Output = $stdout; ExitCode = $proc.ExitCode }
}

function Get-GitFileContent([string]$repoPath, [string]$filePath, [string]$ref) {
    $gitPath = $filePath -replace '\\', '/'
    $result = Invoke-Git -WorkDir $repoPath -Arguments @("show", "${ref}:${gitPath}")
    if ($result.ExitCode -ne 0) { return $null }
    return $result.Output
}

function Test-HasUncommittedChanges([string]$repoPath) {
    $result = Invoke-Git -WorkDir $repoPath -Arguments @("status", "--porcelain")
    return ($result.ExitCode -eq 0 -and $result.Output.Trim().Length -gt 0)
}

# -- 0. Swagger generation -----------------------------------------------------

function Update-SwaggerJson {
    Write-Section "Swagger Generation"

    $swaggerTarget = Join-Path $config.RestApi.Repo $config.RestApi.SwaggerPath

    if ($SwaggerJsonPath) {
        $resolvedPath = Resolve-Path $SwaggerJsonPath -ErrorAction SilentlyContinue
        if (-not $resolvedPath) {
            Write-Warn "pre-generated swagger.json not found: $SwaggerJsonPath"
            return
        }

        $swaggerDir = Split-Path $swaggerTarget -Parent
        if (-not (Test-Path $swaggerDir)) {
            New-Item -ItemType Directory -Path $swaggerDir -Force | Out-Null
        }

        Copy-Item -Path $resolvedPath.Path -Destination $swaggerTarget -Force
        Write-Ok "using pre-generated swagger.json from $($resolvedPath.Path)"
        return
    }

    $backendRepo = $config.BackendServer.Repo
    $serverCsproj = Join-Path $backendRepo $config.BackendServer.Project
    $swaggerDoc = $config.BackendServer.SwaggerDoc

    if (-not (Test-Path $serverCsproj)) {
        Write-Warn "server project not found: $serverCsproj"
        return
    }

    $swashCheck = dotnet tool list -g 2>&1
    $hasSwash = @($swashCheck) | Where-Object { $_ -match "swashbuckle" }
    if (-not $hasSwash) {
        Write-Host "  installing Swashbuckle CLI..." -ForegroundColor DarkGray
        $installOut = dotnet tool install -g Swashbuckle.AspNetCore.Cli 2>&1
        if ($LASTEXITCODE -ne 0) {
            Write-Warn "failed to install Swashbuckle CLI -- swagger.json will not be regenerated"
            return
        }
    }

    Write-Host "  building Backend server..." -ForegroundColor DarkGray
    $buildOut = dotnet build $serverCsproj -c Release 2>&1
    if ($LASTEXITCODE -ne 0) {
        Write-Warn "backend build failed -- swagger.json will not be regenerated"
        return
    }

    $serverDir = Split-Path $serverCsproj -Parent
    $assemblyName = $config.BackendServer.Assembly
    $assembly = Get-ChildItem -Path $serverDir -Filter $assemblyName -Recurse -ErrorAction SilentlyContinue |
        Where-Object { $_.FullName -match "bin[\\/]Release" } |
        Sort-Object LastWriteTime -Descending |
        Select-Object -First 1

    if (-not $assembly) {
        Write-Warn "server assembly not found: $assemblyName"
        return
    }

    Write-Host "  generating swagger.json..." -ForegroundColor DarkGray
    $swaggerDir = Split-Path $swaggerTarget -Parent
    if (-not (Test-Path $swaggerDir)) {
        New-Item -ItemType Directory -Path $swaggerDir -Force | Out-Null
    }

    $pinfo = New-Object System.Diagnostics.ProcessStartInfo
    $pinfo.FileName = "swagger"
    $pinfo.Arguments = "tofile --output `"$swaggerTarget`" `"$($assembly.FullName)`" $swaggerDoc"
    $pinfo.RedirectStandardOutput = $true
    $pinfo.RedirectStandardError = $true
    $pinfo.UseShellExecute = $false
    $pinfo.CreateNoWindow = $true
    $proc = [System.Diagnostics.Process]::Start($pinfo)
    $stdout = $proc.StandardOutput.ReadToEnd()
    $stderr = $proc.StandardError.ReadToEnd()
    $proc.WaitForExit()

    if ($proc.ExitCode -ne 0) {
        Write-Warn "swagger generation failed: $stderr"
        return
    }

    Write-Ok "swagger.json regenerated from $($assembly.Name)"
}

# -- 1. REST API -- swagger.json comparison ------------------------------------

function Compare-SwaggerBreakingChanges {
    Write-Section "REST API (Provider) -- swagger.json"

    $repo = $config.RestApi.Repo
    $swaggerRel = $config.RestApi.SwaggerPath
    $swaggerFull = Join-Path $repo $swaggerRel

    if (-not (Test-Path $swaggerFull)) {
        Write-Warn "swagger.json not found: $swaggerFull"
        return
    }

    if (-not (Test-HasUncommittedChanges $repo)) {
        Write-Ok "no uncommitted changes in $repo"
        return
    }

    $baselineJson = Get-GitFileContent $repo $swaggerRel $BaselineRef
    if (-not $baselineJson) {
        Write-Warn "cannot read $swaggerRel from $BaselineRef"
        return
    }

    $baseline = $baselineJson | ConvertFrom-Json
    $current  = Get-Content $swaggerFull -Raw | ConvertFrom-Json

    $found = $false

    $baselinePaths = @{}
    $bPaths = Get-SafeProperty $baseline 'paths'
    if ($bPaths) { $bPaths.PSObject.Properties | ForEach-Object { $baselinePaths[$_.Name] = $_.Value } }
    $currentPaths = @{}
    $cPaths = Get-SafeProperty $current 'paths'
    if ($cPaths) { $cPaths.PSObject.Properties | ForEach-Object { $currentPaths[$_.Name] = $_.Value } }

    foreach ($path in $baselinePaths.Keys) {
        if (-not $currentPaths.ContainsKey($path)) {
            Write-Breaking "endpoint removed: $path"
            $found = $true
            continue
        }

        $baseMethods = $baselinePaths[$path].PSObject.Properties | Where-Object { $_.Name -in @('get','post','put','delete','patch') }
        $currMethods = @{}
        $currentPaths[$path].PSObject.Properties | Where-Object { $_.Name -in @('get','post','put','delete','patch') } |
            ForEach-Object { $currMethods[$_.Name] = $_.Value }

        foreach ($method in $baseMethods) {
            if (-not $currMethods.ContainsKey($method.Name)) {
                Write-Breaking "method removed: $($method.Name.ToUpper()) $path"
                $found = $true
                continue
            }

            $baseOp = $method.Value
            $currOp = $currMethods[$method.Name]

            $baseParams = @()
            $bpVal = Get-SafeProperty $baseOp 'parameters'
            if ($bpVal) { $baseParams = @($bpVal) }
            $currParams = @()
            $cpVal = Get-SafeProperty $currOp 'parameters'
            if ($cpVal) { $currParams = @($cpVal) }

            foreach ($bp in $baseParams) {
                $paramKey = $bp.name + "|" + $bp.in
                $match = $currParams | Where-Object { ($_.name + "|" + $_.in) -eq $paramKey }
                if (-not $match) {
                    Write-Breaking "parameter removed: $($bp.name) ($($bp.in)) from $($method.Name.ToUpper()) $path"
                    $found = $true
                }
                elseif ((Get-SafeProperty $match 'required') -eq $true -and (Get-SafeProperty $bp 'required') -ne $true) {
                    Write-Breaking "parameter now required: $($bp.name) in $($method.Name.ToUpper()) $path"
                    $found = $true
                }
            }

            foreach ($cp in $currParams) {
                $paramKey = $cp.name + "|" + $cp.in
                $match = $baseParams | Where-Object { ($_.name + "|" + $_.in) -eq $paramKey }
                if (-not $match -and (Get-SafeProperty $cp 'required') -eq $true) {
                    Write-Breaking "new required parameter: $($cp.name) ($($cp.in)) in $($method.Name.ToUpper()) $path"
                    $found = $true
                }
            }

            $baseResp = Get-SafeProperty $baseOp 'responses'
            $currResp = Get-SafeProperty $currOp 'responses'
            if ($baseResp -and $currResp) {
                $baseResponses = $baseResp.PSObject.Properties.Name
                $currResponses = $currResp.PSObject.Properties.Name
                foreach ($code in $baseResponses) {
                    if ($code -match "^2\d{2}" -and $code -notin $currResponses) {
                        Write-Breaking "success response removed: $code from $($method.Name.ToUpper()) $path"
                        $found = $true
                    }
                }
            }
        }
    }

    foreach ($path in $currentPaths.Keys) {
        if (-not $baselinePaths.ContainsKey($path)) {
            Write-Ok "new endpoint: $path"
        }
    }

    $schemaKey = if (Get-SafeProperty $baseline 'components') { "components" } else { "definitions" }
    $baseSchemas = @{}
    $currSchemas = @{}

    $baseComp = Get-SafeProperty $baseline 'components'
    if ($schemaKey -eq "components" -and $baseComp -and (Get-SafeProperty $baseComp 'schemas')) {
        (Get-SafeProperty $baseComp 'schemas').PSObject.Properties | ForEach-Object { $baseSchemas[$_.Name] = $_.Value }
    }
    elseif ($schemaKey -eq "definitions" -and (Get-SafeProperty $baseline 'definitions')) {
        (Get-SafeProperty $baseline 'definitions').PSObject.Properties | ForEach-Object { $baseSchemas[$_.Name] = $_.Value }
    }

    $currComp = Get-SafeProperty $current 'components'
    if ($schemaKey -eq "components" -and $currComp -and (Get-SafeProperty $currComp 'schemas')) {
        (Get-SafeProperty $currComp 'schemas').PSObject.Properties | ForEach-Object { $currSchemas[$_.Name] = $_.Value }
    }
    elseif ($schemaKey -eq "definitions" -and (Get-SafeProperty $current 'definitions')) {
        (Get-SafeProperty $current 'definitions').PSObject.Properties | ForEach-Object { $currSchemas[$_.Name] = $_.Value }
    }

    foreach ($name in $baseSchemas.Keys) {
        if (-not $currSchemas.ContainsKey($name)) {
            Write-Breaking "schema removed: $name"
            $found = $true
            continue
        }

        $baseProps = @{}
        $bsPropVal = Get-SafeProperty $baseSchemas[$name] 'properties'
        if ($bsPropVal) { $bsPropVal.PSObject.Properties | ForEach-Object { $baseProps[$_.Name] = $_.Value } }
        $currProps = @{}
        $csPropVal = Get-SafeProperty $currSchemas[$name] 'properties'
        if ($csPropVal) { $csPropVal.PSObject.Properties | ForEach-Object { $currProps[$_.Name] = $_.Value } }

        foreach ($prop in $baseProps.Keys) {
            if (-not $currProps.ContainsKey($prop)) {
                Write-Breaking "property removed: $($name).$prop"
                $found = $true
            }
            else {
                $baseType = Get-SafeProperty $baseProps[$prop] 'type'
                $currType = Get-SafeProperty $currProps[$prop] 'type'
                if ($baseType -and $currType -and $baseType -ne $currType) {
                    Write-Breaking "property type changed: $($name).$prop ($baseType -> $currType)"
                    $found = $true
                }
            }
        }

        $baseRequired = @()
        $baseReqVal = Get-SafeProperty $baseSchemas[$name] 'required'
        if ($baseReqVal) { $baseRequired = @($baseReqVal) }
        $currRequired = @()
        $currReqVal = Get-SafeProperty $currSchemas[$name] 'required'
        if ($currReqVal) { $currRequired = @($currReqVal) }

        $newRequired = $currRequired | Where-Object { $_ -notin $baseRequired }
        foreach ($req in $newRequired) {
            Write-Breaking "property now required: $($name).$req"
            $found = $true
        }
    }

    if (-not $found) {
        Write-Ok "no breaking changes detected in REST API"
        $script:report.Add("- No breaking changes detected.")
    }
}

