<#
.SYNOPSIS
    Detects breaking changes in uncommitted code across all PayrollEngine APIs
    and documents them in RELEASE_NOTES.md.

.DESCRIPTION
    Compares the current (uncommitted) state against the last Git commit for:
      0. Swagger Generation       -- regenerate swagger.json from Backend build
      1. REST API (Provider)      -- swagger.json diff
      2. Backend API Models       -- public .NET API surface diff
      3. Scripting API (Regulator) -- public .NET API surface diff
      4. Client Services (Automator) -- public .NET API surface diff
      5. RELEASE_NOTES.md         -- append breaking changes to release notes

    No external tools required -- uses PowerShell + dotnet SDK only.
    Compatible with PowerShell 5.1+ and .NET 9/10 assemblies.

.PARAMETER ReposRoot
    Root folder containing all PayrollEngine repositories.

.PARAMETER BaselineRef
    Git ref to compare against (default: HEAD).

.PARAMETER ReportPath
    Optional path for the output report file.

.PARAMETER SwaggerJsonPath
    Optional path to a pre-generated swagger.json file. When provided,
    the script skips Swashbuckle CLI generation and uses this file
    directly for the REST API diff. Useful in CI where swagger.json
    has already been generated by a prior build step.

.PARAMETER ConfirmReleaseNotes
    Prompts for confirmation before updating RELEASE_NOTES.md.
    Default behavior (without this switch) is automatic update.

.PARAMETER SkipReleaseNotes
    Skips the RELEASE_NOTES.md update entirely.

.EXAMPLE
    .\Update-BreakingChanges.ps1
    .\Update-BreakingChanges.ps1 -BaselineRef "v0.9.0" -ReportPath ".\breaking-changes.md"
    .\Update-BreakingChanges.ps1 -ConfirmReleaseNotes
    .\Update-BreakingChanges.ps1 -SkipReleaseNotes
    .\Update-BreakingChanges.ps1 -SwaggerJsonPath "docs\swagger.json"
#>

[CmdletBinding()]
param(
    [string]$ReposRoot = (Resolve-Path "$PSScriptRoot\..\..\..").Path,
    [string]$BaselineRef = "HEAD",
    [string]$ReportPath,
    [string]$SwaggerJsonPath,
    [switch]$ConfirmReleaseNotes,
    [switch]$SkipReleaseNotes
)

Set-StrictMode -Version Latest
$ErrorActionPreference = "Stop"

