function Initialize-ApiExtractorProject {
    $toolDir = Join-Path ([System.IO.Path]::GetTempPath()) "bc-api-extractor"
    $csproj  = Join-Path $toolDir "ApiExtractor.csproj"
    $program = Join-Path $toolDir "Program.cs"

    if (Test-Path $toolDir) {
        Remove-Item $toolDir -Recurse -Force -ErrorAction SilentlyContinue
    }
    New-Item -ItemType Directory -Path $toolDir -Force | Out-Null

    $sdkVersion = dotnet --version 2>$null
    if ($sdkVersion -match "^10\.") { $tfm = "net10.0"; $pkgVer = "10.0.*" }
    elseif ($sdkVersion -match "^9\.") { $tfm = "net9.0"; $pkgVer = "9.0.*" }
    else { $tfm = "net8.0"; $pkgVer = "9.0.*" }

    @"
<Project Sdk="Microsoft.NET.Sdk">
  <PropertyGroup>
    <OutputType>Exe</OutputType>
    <TargetFramework>$tfm</TargetFramework>
  </PropertyGroup>
  <ItemGroup>
    <PackageReference Include="System.Reflection.MetadataLoadContext" Version="$pkgVer" />
  </ItemGroup>
</Project>
"@ | Set-Content -Path $csproj -Encoding UTF8

    @'
using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Reflection;
using System.Runtime.InteropServices;

var assemblyPath = args[0];
var signatures = new SortedSet<string>();

var assemblyDir = Path.GetDirectoryName(Path.GetFullPath(assemblyPath))!;
var runtimeDir = RuntimeEnvironment.GetRuntimeDirectory();
var resolver = new PathAssemblyResolver(
    Directory.GetFiles(runtimeDir, "*.dll")
        .Concat(Directory.GetFiles(assemblyDir, "*.dll"))
        .Distinct());

using var mlc = new MetadataLoadContext(resolver);
var assembly = mlc.LoadFromAssemblyPath(Path.GetFullPath(assemblyPath));

foreach (var type in assembly.GetExportedTypes().OrderBy(t => t.FullName))
{
    var typeKind = type.IsInterface ? "interface" :
                   type.IsEnum ? "enum" :
                   type.IsValueType ? "struct" :
                   (type.IsAbstract && type.IsSealed) ? "static class" :
                   type.IsAbstract ? "abstract class" : "class";

    var baseName = type.BaseType != null && type.BaseType.FullName != "System.Object"
        ? " : " + type.BaseType.Name : "";

    signatures.Add($"{typeKind} {type.FullName}{baseName}");

    if (type.IsEnum)
    {
        foreach (var field in type.GetFields(BindingFlags.Public | BindingFlags.Static).OrderBy(f => f.Name))
        {
            var rawValue = field.GetRawConstantValue();
            signatures.Add($"  {type.FullName}.{field.Name} = {rawValue}");
        }
        continue;
    }

    var flags = BindingFlags.Public | BindingFlags.Instance | BindingFlags.Static | BindingFlags.DeclaredOnly;

    foreach (var ctor in type.GetConstructors(BindingFlags.Public | BindingFlags.Instance).OrderBy(c => c.ToString()))
    {
        var p = string.Join(", ", ctor.GetParameters().Select(x => $"{x.ParameterType.Name} {x.Name}"));
        signatures.Add($"  {type.Name}({p})");
    }

    foreach (var m in type.GetMethods(flags).Where(m => !m.IsSpecialName).OrderBy(m => m.Name).ThenBy(m => m.ToString()))
    {
        var s = m.IsStatic ? "static " : "";
        var p = string.Join(", ", m.GetParameters().Select(x => $"{x.ParameterType.Name} {x.Name}"));
        signatures.Add($"  {s}{m.ReturnType.Name} {type.FullName}.{m.Name}({p})");
    }

    foreach (var prop in type.GetProperties(flags).OrderBy(p => p.Name))
    {
        var s = (prop.GetMethod?.IsStatic ?? prop.SetMethod?.IsStatic ?? false) ? "static " : "";
        var acc = new List<string>();
        if (prop.CanRead) acc.Add("get");
        if (prop.CanWrite) acc.Add("set");
        signatures.Add($"  {s}{prop.PropertyType.Name} {type.FullName}.{prop.Name} {{ {string.Join("; ", acc)} }}");
    }

    foreach (var evt in type.GetEvents(flags).OrderBy(e => e.Name))
    {
        signatures.Add($"  event {evt.EventHandlerType?.Name} {type.FullName}.{evt.Name}");
    }
}

foreach (var sig in signatures)
    Console.WriteLine(sig);
'@ | Set-Content -Path $program -Encoding UTF8

    Write-Host "  preparing API extractor tool..." -ForegroundColor DarkGray
    $buildOut = dotnet build $csproj -c Release 2>&1
    if ($LASTEXITCODE -ne 0) {
        Write-Warn "failed to build API extractor: $buildOut"
        return $null
    }

    return $toolDir
}

function Get-PublicApiSurface([string]$assemblyPath, [string]$toolDir) {
    if (-not (Test-Path $assemblyPath)) { return @() }
    if (-not $toolDir -or -not (Test-Path $toolDir)) { return @() }

    $csproj = Join-Path $toolDir "ApiExtractor.csproj"

    $pinfo = New-Object System.Diagnostics.ProcessStartInfo
    $pinfo.FileName = "dotnet"
    $pinfo.Arguments = "run --project `"$csproj`" -c Release --no-build -- `"$assemblyPath`""
    $pinfo.RedirectStandardOutput = $true
    $pinfo.RedirectStandardError = $true
    $pinfo.UseShellExecute = $false
    $pinfo.CreateNoWindow = $true
    $proc = [System.Diagnostics.Process]::Start($pinfo)
    $stdout = $proc.StandardOutput.ReadToEnd()
    $null = $proc.StandardError.ReadToEnd()
    $proc.WaitForExit()

    if ($proc.ExitCode -ne 0) {
        Write-Warn "API extraction failed for $assemblyPath"
        return @()
    }

    $lines = @($stdout -split "`n") | ForEach-Object { $_.TrimEnd("`r") } | Where-Object {
        $_ -match "^(class|interface|enum|struct|static class|abstract class|  )"
    }
    return @($lines)
}

function Compare-DotNetApiSurface {
    param(
        [string]$Label,
        [string]$RepoPath,
        [string[]]$ProjectNames,
        [string]$ToolDir
    )

    Write-Section "$Label"

    if (-not (Test-Path $RepoPath)) {
        Write-Warn "repository not found: $RepoPath"
        return
    }

    if (-not (Test-HasUncommittedChanges $RepoPath)) {
        Write-Ok "no uncommitted changes in $RepoPath"
        return
    }

    foreach ($project in $ProjectNames) {
        Write-Host "`n  Analyzing: $project" -ForegroundColor DarkCyan

        $csproj = Get-ChildItem -Path $RepoPath -Filter "$project.csproj" -Recurse -ErrorAction SilentlyContinue | Select-Object -First 1
        if (-not $csproj) {
            Write-Warn "project file not found: $project.csproj"
            continue
        }

        Write-Host "  building current state..." -ForegroundColor DarkGray
        $currentBuildOut = Join-Path ([System.IO.Path]::GetTempPath()) "bc-current-$project"
        if (Test-Path $currentBuildOut) { Remove-Item $currentBuildOut -Recurse -Force }

        $buildOut = dotnet publish $csproj.FullName -c Release -o $currentBuildOut 2>&1
        if ($LASTEXITCODE -ne 0) {
            Write-Warn "build failed for current state of $project"
            continue
        }

        $currentDll = Join-Path $currentBuildOut "$project.dll"
        if (-not (Test-Path $currentDll)) {
            Write-Warn "assembly not found after build: $currentDll"
            continue
        }

        $currentApi = @(Get-PublicApiSurface $currentDll $ToolDir)

        # -- Step 2: build baseline from git archive (no working tree changes) --
        Write-Host "  building baseline ($BaselineRef) via git archive..." -ForegroundColor DarkGray
        $baselineApi = @()
        $baselineSrcDir = Join-Path ([System.IO.Path]::GetTempPath()) "bc-baseline-src-$project"
        $baselineBuildOut = Join-Path ([System.IO.Path]::GetTempPath()) "bc-baseline-$project"
        if (Test-Path $baselineSrcDir) { Remove-Item $baselineSrcDir -Recurse -Force }
        if (Test-Path $baselineBuildOut) { Remove-Item $baselineBuildOut -Recurse -Force }
        New-Item -ItemType Directory -Path $baselineSrcDir -Force | Out-Null

        # extract baseline source into temp dir (safe, no stash)
        $pinfo = New-Object System.Diagnostics.ProcessStartInfo
        $pinfo.FileName = "git"
        $pinfo.Arguments = "archive $BaselineRef"
        $pinfo.WorkingDirectory = $RepoPath
        $pinfo.RedirectStandardOutput = $true
        $pinfo.RedirectStandardError = $true
        $pinfo.UseShellExecute = $false
        $pinfo.CreateNoWindow = $true
        $archiveProc = [System.Diagnostics.Process]::Start($pinfo)
        $tarFile = Join-Path $baselineSrcDir "archive.tar"
        # read binary tar stream to file
        $fs = [System.IO.File]::Create($tarFile)
        $archiveProc.StandardOutput.BaseStream.CopyTo($fs)
        $fs.Close()
        $null = $archiveProc.StandardError.ReadToEnd()
        $archiveProc.WaitForExit()

        if ($archiveProc.ExitCode -eq 0 -and (Test-Path $tarFile) -and (Get-Item $tarFile).Length -gt 0) {
            # extract tar
            $tarPinfo = New-Object System.Diagnostics.ProcessStartInfo
            $tarPinfo.FileName = "tar"
            $tarPinfo.Arguments = "xf `"$tarFile`" -C `"$baselineSrcDir`""
            $tarPinfo.RedirectStandardOutput = $true
            $tarPinfo.RedirectStandardError = $true
            $tarPinfo.UseShellExecute = $false
            $tarPinfo.CreateNoWindow = $true
            $tarProc = [System.Diagnostics.Process]::Start($tarPinfo)
            $null = $tarProc.StandardOutput.ReadToEnd()
            $null = $tarProc.StandardError.ReadToEnd()
            $tarProc.WaitForExit()

            if ($tarProc.ExitCode -eq 0) {
                $baselineCsproj = Get-ChildItem -Path $baselineSrcDir -Filter "$project.csproj" -Recurse -ErrorAction SilentlyContinue | Select-Object -First 1
                if ($baselineCsproj) {
                    $buildOut = dotnet publish $baselineCsproj.FullName -c Release -o $baselineBuildOut 2>&1
                    if ($LASTEXITCODE -eq 0) {
                        $baselineDll = Join-Path $baselineBuildOut "$project.dll"
                        if (Test-Path $baselineDll) {
                            $baselineApi = @(Get-PublicApiSurface $baselineDll $ToolDir)
                        } else {
                            Write-Warn "baseline assembly not found: $baselineDll"
                        }
                    } else {
                        Write-Warn "baseline build failed for $project"
                    }
                } else {
                    Write-Warn "baseline csproj not found in archive for $project"
                }
            } else {
                Write-Warn "tar extraction failed for baseline archive"
            }
        } else {
            Write-Warn "git archive failed for $BaselineRef -- baseline comparison skipped"
        }

        if (@($currentApi).Count -eq 0 -and @($baselineApi).Count -eq 0) {
            Write-Warn "could not extract API surface for $project"
            continue
        }

        $found = $false

        $removed = @($baselineApi | Where-Object { $_ -notin $currentApi })
        foreach ($sig in $removed) {
            $trimmed = $sig.Trim()
            if (-not $sig.StartsWith("  ")) {
                Write-Breaking "type removed: $trimmed"
            }
            else {
                Write-Breaking "member removed: $trimmed"
            }
            $found = $true
        }

        $added = @($currentApi | Where-Object { $_ -notin $baselineApi })
        $addedTypes = @($added | Where-Object { $_ -and -not $_.StartsWith("  ") })
        $addedMembers = @($added | Where-Object { $_ -and $_.StartsWith("  ") })

        if ($addedTypes.Count -gt 0) {
            Write-Ok "$($addedTypes.Count) new type(s) added"
        }
        if ($addedMembers.Count -gt 0) {
            Write-Ok "$($addedMembers.Count) new member(s) added"
        }

        if (-not $found) {
            Write-Ok "no breaking changes detected in $project"
            $script:report.Add("- No breaking changes detected.")
        }

        Remove-Item $currentBuildOut -Recurse -Force -ErrorAction SilentlyContinue
        Remove-Item $baselineBuildOut -Recurse -Force -ErrorAction SilentlyContinue
        Remove-Item $baselineSrcDir -Recurse -Force -ErrorAction SilentlyContinue
    }
}

# -- 5. RELEASE_NOTES.md update ------------------------------------------------

function Format-ForReleaseNotes([string]$message) {
    if ($message -match '^member removed:') {
        $sig = $message.Substring('member removed:'.Length).Trim()
        $sig = [regex]::Replace($sig, '`\d+', '')
        if ($sig -match '(\w+)\.(\w+)\(') {
            return "removed ``$($Matches[1]).$($Matches[2])``"
        }
        if ($sig -match '(\w+)\.(\w+)\s*\{') {
            return "removed ``$($Matches[1]).$($Matches[2])``"
        }
        $clean = $sig -replace '\(.*', ''
        $segments = $clean.Trim() -split '\.'
        if ($segments.Count -ge 2) {
            return "removed ``$($segments[-2]).$($segments[-1])``"
        }
        return "removed member (signature changed)"
    }

    if ($message -match '^type removed:') {
        $sig = $message.Substring('type removed:'.Length).Trim()
        $sig = [regex]::Replace($sig, '`\d+', '')
        $segments = $sig.Trim() -split '\.'
        $typeName = $segments[-1] -replace '\s.*', ''
        return "removed type ``$typeName``"
    }

    return $message
}

function Update-ReleaseNotes {
    if ($SkipReleaseNotes) { return }
    if ($script:sectionBreaks.Count -eq 0) { return }

    $notesPath = $script:releaseNotesPath
    if (-not (Test-Path $notesPath)) {
        Write-Warn "RELEASE_NOTES.md not found: $notesPath"
        return
    }

    $grouped = @{}
    foreach ($key in $script:sectionBreaks.Keys) {
        $map = $script:releaseNotesMap[$key]
        if (-not $map) { continue }
        $header = $map.Header
        if (-not $grouped.ContainsKey($header)) {
            $grouped[$header] = @{
                Url       = $map.Url
                IsLibrary = $map.IsLibrary
                Entries   = [System.Collections.Generic.List[string]]::new()
            }
        }
        foreach ($msg in $script:sectionBreaks[$key]) {
            $formatted = Format-ForReleaseNotes $msg
            $entry = "  - $formatted **breaking change**"
            if ($entry -notin $grouped[$header].Entries) {
                $grouped[$header].Entries.Add($entry)
            }
        }
    }

    if ($grouped.Count -eq 0) { return }

    Write-Section "RELEASE_NOTES.md Update"
    foreach ($header in $grouped.Keys) {
        $info = $grouped[$header]
        Write-Host "  [$header]" -ForegroundColor Cyan
        foreach ($entry in $info.Entries) {
            Write-Host "  $entry" -ForegroundColor DarkGray
        }
    }

    if ($ConfirmReleaseNotes) {
        Write-Host ""
        $answer = Read-Host "  Update RELEASE_NOTES.md with the above entries? (y/N)"
        if ($answer -ne 'y' -and $answer -ne 'Y') {
            Write-Host "  Skipped RELEASE_NOTES.md update." -ForegroundColor Yellow
            return
        }
    }

    $lines = @(Get-Content $notesPath -Encoding UTF8)
    $result = [System.Collections.Generic.List[string]]::new()
    $insertedSections = @{}

    for ($i = 0; $i -lt $lines.Count; $i++) {
        $line = $lines[$i]
        $result.Add($line)

        foreach ($header in $grouped.Keys) {
            $pattern = "- [$header]("
            if ($line.TrimStart().StartsWith($pattern) -and -not $insertedSections.ContainsKey($header)) {
                foreach ($entry in $grouped[$header].Entries) {
                    $result.Add($entry)
                }
                $insertedSections[$header] = $true
            }
        }
    }

    foreach ($header in $grouped.Keys) {
        if ($insertedSections.ContainsKey($header)) { continue }

        $info = $grouped[$header]
        $sectionHeader = "- [$header]($($info.Url))"

        if ($info.IsLibrary) {
            $libIdx = -1
            for ($i = 0; $i -lt $result.Count; $i++) {
                if ($result[$i] -match '^###\s+Libraries') { $libIdx = $i; break }
            }
            if ($libIdx -ge 0) {
                $insertAt = $libIdx + 1
                while ($insertAt -lt $result.Count -and $result[$insertAt].Trim() -eq '') { $insertAt++ }
                $newLines = @("", $sectionHeader)
                foreach ($entry in $info.Entries) { $newLines += $entry }
                for ($j = $newLines.Count - 1; $j -ge 0; $j--) { $result.Insert($insertAt, $newLines[$j]) }
            }
            else {
                $result.Add("")
                $result.Add("### Libraries")
                $result.Add("")
                $result.Add($sectionHeader)
                foreach ($entry in $info.Entries) { $result.Add($entry) }
            }
        }
        else {
            $libIdx = -1
            for ($i = 0; $i -lt $result.Count; $i++) {
                if ($result[$i] -match '^###\s+Libraries') { $libIdx = $i; break }
            }
            if ($libIdx -ge 0) {
                $newLines = @("", $sectionHeader)
                foreach ($entry in $info.Entries) { $newLines += $entry }
                $newLines += ""
                for ($j = $newLines.Count - 1; $j -ge 0; $j--) { $result.Insert($libIdx, $newLines[$j]) }
            }
            else {
                $result.Add("")
                $result.Add($sectionHeader)
                foreach ($entry in $info.Entries) { $result.Add($entry) }
            }
        }
        $insertedSections[$header] = $true
    }

    $result -join "`n" | Set-Content -Path $notesPath -Encoding UTF8 -NoNewline
    Write-Ok "RELEASE_NOTES.md updated ($($script:breakingCount) entries added)"
    $script:report.Add("`n## RELEASE_NOTES.md`n")
    $script:report.Add("- Updated: $notesPath")
}

